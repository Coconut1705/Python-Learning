# Các kiểu dữ liệu số 
integer_number = 10 
float_number = 3.14 
complex_number = 2 + 3j 

# Các kiểu dữ liệu chuỗi 
string_text = "Hello, World!" 
list_fruits = ["apple", "banana", "cherry"] 
tuple_coordinates = (10.0, 20.0) 

# Ánh xạ kiểu 
dict_student = {"name": "Alice", "age": 21, "course": "Computer Science"} 

# Tập hợp kiểu 
set_fruits = {"apple", "banana", "cherry"} 
frozenset_fruits = frozenset({"apple", "banana", "cherry"}) 

# Kiểu Boolean 
bool_is_active = True 
bool_is_student = False 

# Kiểu nhị phân 
bytes_data = b"Hello" 
bytearray_data = bytearray(5) 
memoryview_data = memoryview(bytearray_data) 

# In biến 
print("Số nguyên:", integer_number) 
print("Số thực:", float_number) 
print("Số phức:", complex_number) 
print("Chuỗi:", string_text) 
print("Danh sách:", list_fruits) 
print("Tuple:", tuple_coordinates) 
print("Từ điển:", dict_student) 
print("Tập hợp:", set_fruits) 
print("Tập hợp đông lạnh:", frozenset_fruits) 
print("Boolean:", bool_is_active, bool_is_student) 
print("Byte:", bytes_data) 
print("Mảng byte:", bytearray_data) 
print("Chế độ xem bộ nhớ:", memoryview_data)